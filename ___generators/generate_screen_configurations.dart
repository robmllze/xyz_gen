void main(List<String> arguments) async {
  await generateScreenConfigurationsApp([
    if (arguments.isNotEmpty)
      ...arguments
    else ...[
      // Template file path.
      "-t",
      "___generators/templates/generate_screen_configurations/default_screen_configuration_template.dart.md",
      // Root directories.
      "-r",
      [
        "$APP_FOLDER/lib",
      ].join("&"),
      // Sub-directories.
      "-s",
      [
        "screens",
      ].join("&"),
    ],
  ]);
}
