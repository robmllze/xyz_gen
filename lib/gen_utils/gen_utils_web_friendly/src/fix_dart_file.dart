/// Applies fixes to the dart file at [filePath].
Future<void> fixDartFile(String filePath) async {
  try {
    final fixedPath = toLocalPathFormat(filePath);
    await Process.run("dart", ["fix", "--apply", fixedPath]);
  } catch (_) {}
}
