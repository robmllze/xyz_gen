import 'package:collection/collection.dart' show DeepCollectionEquality;

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

class DeepIterable<T> {
  Iterable<T> values;

  DeepIterable._([this.values = const []]);

  @override
  bool operator ==(Object other) {
    if (identical(this, other)) return true;
    return other is DeepIterable<T> && const DeepCollectionEquality().equals(other.values, values);
  }

  @override
  int get hashCode => const DeepCollectionEquality().hash(values);
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

extension DeepIterableExtension<T> on Iterable<T> {
  DeepIterable<T> get deep => DeepIterable<T>._(this);
}
