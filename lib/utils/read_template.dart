import 'dart:io';

import 'helpers.dart';

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

Future<String> readTemplate(String filePath) async {
  final file = File(filePath);
  final input = await file.readAsString();
  final output = input.replaceFirst("````dart", "").replaceLast("````", "").trim();
  return output;
}
