import 'dart:collection' show Queue;
import 'package:collection/collection.dart' show DeepCollectionEquality;

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

class DeepQueue<T> {
  Queue<T> values;

  DeepQueue._([Iterable<T>? source]) : values = source == null ? Queue<T>() : Queue<T>.from(source);

  @override
  bool operator ==(Object other) {
    if (identical(this, other)) return true;
    return other is DeepQueue<T> && const DeepCollectionEquality().equals(other.values, values);
  }

  @override
  int get hashCode => const DeepCollectionEquality().hash(values);
}

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

extension DeepQueueExtension<T> on Queue<T> {
  DeepQueue<T> get deep => DeepQueue<T>._(this);
}
