import 'package:path/path.dart' as p;

// ░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░░

bool isDartFile(String input) {
  return input.toLowerCase().endsWith(".dart");
}

bool isGeneratedDartFile(String input) {
  return input.toLowerCase().endsWith(".g.dart");
}

String getFileName(String path) {
  return p.basename(getFixedPath(path));
}

String getFixedPath(String path) {
  return path.split(RegExp(r"[\\/]")).join(p.separator);
}

bool isPrivateFile(String path) {
  final fileName = getFileName(path);
  return fileName.startsWith("_");
}

bool isScreenFile(String path) {
  final fileName = getFileName(path);
  return fileName.toLowerCase().startsWith("screen_");
}
