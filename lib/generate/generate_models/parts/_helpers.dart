Map<String, String> _getKeyNames(Iterable<String> parameterKeys) {
  return Map.fromEntries(
    parameterKeys.map(
      (k) => MapEntry(
        k,
        k.toSnakeCase(),
      ),
    ),
  );
}

Map<String, String> _getKeyConstNames(Iterable<String> parameterKeys) {
  return Map.fromEntries(
    parameterKeys.map(
      (e) => MapEntry(
        e,
        "K_${e.toSnakeCase().toUpperCase()}",
      ),
    ),
  );
}
