/// Prints the usage information for the given [parser].
void printArgParserUsage(
  ArgParser parser, {
  required String title,
  required String description,
}) {
  final topBorderLine = '‚ïî${'‚ïê' * (title.length - 1)}‚ïó';
  final bottomBorderLine = '‚ïö${'‚ïê' * (title.length - 1)}‚ïù';
  final titleLine = '‚ïë $title ‚ïë';
  printLightCyan(
    [
      '',
      topBorderLine,
      titleLine,
      bottomBorderLine,
      '',
      description,
      '',
      parser.usage,
      '',
    ].join('\n'),
  );
}
